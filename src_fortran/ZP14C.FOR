      SUBROUTINE ZP14C(A,B,C,ROOT1,ROOT2,IERR)
      DIMENSION AA(2),BB(2),CC(2),ZSA(2),ZLA(2)
      INTEGER IERR,IS
      REAL AA,AR,AI,BB,BR,BI,CC,CR,CI,ZSA,ZSR,ZSI,ZLA,ZLR,
     1ZLI,ZERO,HALF,SCALE,TEMP,SYS065,SYS059,SNGL,ABS,ALOG,
     1SYS063
      DOUBLE PRECISION DR,DI,D,D1,DBLE
      COMPLEX A,B,C,ROOT1,ROOT2,A0,B0,C0,ZS,ZL,CMPLX,CSQRT
      EQUIVALENCE(AA(1),A0),(BB(1),B0),(CC(1),C0),(ZSA(1),ZS),
     1(ZLA(1),ZL),(AA(1),AR),(BB(1),BR),(CC(1),CR),(ZSA(1),ZSR),
     1(ZLA(1),ZLR),(AA(2),AI),(BB(2),BI),(CC(2),CI),(ZSA(2),ZSI),
     1(ZLA(2),ZLI)
      DATA  SYS063/2./,SYS065/6.931472E-1/,ZERO/0.0/,HALF/
     10.5/,SYS059/3.4E38/
      IERR=0
      A0=A
      B0=-B
      C0=C
      IF(AR.NE.ZERO.OR.AI.NE.ZERO) GO TO 1
      IERR=65
      ZL=CMPLX(SYS059,ZERO)
      ZS=-ZL
      IF(BR.EQ.ZERO.AND.BI.EQ.ZERO) GO TO 7
      IERR=66
      ZS=C0/B0
      GO TO 7
    1 IF(CR.NE.ZERO.OR.CI.NE.ZERO) GO TO 2
      ZS=CMPLX(ZERO,ZERO)
      GO TO 6
    2 IS=(ALOG(AMAX1(ABS(AR),ABS(AI)))+ALOG(AMAX1(ABS(CR),
     1ABS(CI)))+SYS065)/(SYS065+SYS065)
      SCALE=SYS063**IS
      TEMP=AMAX1(ABS(BR),ABS(BI))
      D1=DBLE(TEMP)
      D=D1+SCALE
      D=D-D1
      IF(SNGL(D).EQ.ZERO) GO TO 5
      D=D1+SCALE
      D=D-SCALE
      IF(SNGL(D).NE.ZERO) GO TO 3
      BR=ZERO
      BI=ZERO
      GO TO 4
    3 BR=(BR/SCALE)*HALF
      BI=(BI/SCALE)*HALF
    4 AR=AR/SCALE
      AI=AI/SCALE
      CR=CR/SCALE
      CI=CI/SCALE
      DR=DBLE(BR)**2
      DI=DBLE(BI)*(2.0D0*DBLE(BR))
      ZS=CMPLX(SNGL(((DR-DBLE(BI)**2)-DBLE(AR)*DBLE(CR))+
     1DBLE(AI)*DBLE(CI)),SNGL((DI-DBLE(AI)*DBLE(CR))-
     1DBLE(AR)*DBLE(CI)))
      ZS=CSQRT(ZS)
      IF(DBLE(ZSR)*DBLE(BR)+DBLE(ZSI)*DBLE(BI).LE.ZERO) ZS=-
     1ZS
      B0=B0+ZS
    5 ZS=C0/B0
    6 ZL=B0/A0
    7 ROOT1=ZS
      ROOT2=ZL
      CONTINUE
      IF(IERR.NE.0) CALL UTZP10(IERR,141)
      RETURN
      END
