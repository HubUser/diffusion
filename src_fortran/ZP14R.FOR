      SUBROUTINE ZP14R(A,B,C,ROOT1,ROOT2,IERR)
      INTEGER IERR,IS
      REAL A,B,C,A0,B0,B1,C0,S,SCALE,HALF,ZERO,DD,SYS065,
     1SYS059,ALOG,SNGL,ABS,SYS063
      DOUBLE PRECISION D,D1,DBLE
      COMPLEX ROOT1,ROOT2,ZS,ZL,CMPLX,CONJG
      DATA  SYS063/2./,SYS065/6.931472E-1/,ZERO/0.0/,HALF/
     10.5/,SYS059/3.4E38/
      IERR=0
      A0=A
      B1=-B
      C0=C
      IF(A0.NE.ZERO) GO TO 1
      IERR=65
      ZL=CMPLX(SIGN(SYS059,ZERO))
      ZS=-ZL
      IF(B1.EQ.ZERO) GO TO 6
      IERR=66
      ZS=CMPLX(C0/B1,ZERO)
      GO TO 6
    1 IF(C0.NE.ZERO) GO TO 2
      ZS=CMPLX(ZERO,ZERO)
      GO TO 5
    2 IS=(ALOG(ABS(A0))+ALOG(ABS(C0))+SYS065)/(SYS065+
     1SYS065)
      SCALE=SYS063**IS
      D1=DBLE(ABS(B1))
      D=D1+SCALE
      D=D-D1
      IF(SNGL(D).EQ.ZERO) GO TO 4
      B0=ZERO
      D=D1+SCALE
      D=D-SCALE
      IF(SNGL(D).NE.ZERO) B0=(B1/SCALE)*HALF
      A0=A0/SCALE
      C0=C0/SCALE
      DD=DBLE(B0)**2-DBLE(A0)*DBLE(C0)
      S=SQRT(ABS(DD))
      IF(DD.GT.ZERO) GO TO 3
      ZL=CMPLX(B0/A0,ABS(S/A0))
      ZS=CONJG(ZL)
      GO TO 6
    3 B1=SIGN(S,B0)+B0
    4 ZS=CMPLX(C0/B1,ZERO)
    5 ZL=CMPLX(B1/A0,ZERO)
      IF(ABS(REAL(ZL)).LT.ABS(REAL(ZS))) ZS=-ZL
    6 ROOT1=ZS
      ROOT2=ZL
      CONTINUE
      IF(IERR.NE.0) CALL UTZP10(IERR,14)
      RETURN
      END
