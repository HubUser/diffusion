      Y=ABS(X)
      Z=Y*0.125
      IF(Z.GT.1) GO TO 3
      IF(Z.LE.0) GO TO 2
      X2=4.*Z*Z-2.
      Q3=0.
      Q2=0.
      DO 1 I=1,18
      Q1=Q2
      Q2=Q3
      Q3=X2*Q2-Q1+A(I)
    1 CONTINUE
      SF25R=(Q3-Q1)*0.5*Z
      GO TO 6
    2 SF25R=0.
      GO TO 6
    3 Z=1./Z
      X2=4.*Z*Z-2.
      Q3=0.
      Q2=0.
      DO 4 I=1,18
      Q1=Q2
      Q2=Q3
      Q3=X2*Q2-Q1+C(I)
    4 CONTINUE
      X1=(Q3-Q1)*0.5
      Q3=0.
      Q2=0.
      DO 5 I=1,18
      Q1=Q2
      Q2=Q3
      Q3=X2*Q2-Q1+D(I)
    5 CONTINUE
      FX=(Q3-Q1)*0.5
      X2=COS(Y-SD2PI)
      X3=SIN(Y-SD2PI)
      X4=SYS010/SQRT(Y)
      FX=FX*Z
      SF25R=X4*(X1*X2-FX*X3)
    6 RETURN
      END
